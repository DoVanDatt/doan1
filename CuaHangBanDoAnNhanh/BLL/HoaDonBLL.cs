        public string them(HoaDon a)
        {
            if (hd.KiemTraid(a))
            {
                return "tài khoản bị trùng";
            }
            else
            {
                if (hd.them(a))
                {
                    return "thành công";
                }
                else
                {
                    return "thất bại";
                }


            }
        }

        


        public string xoa(HoaDon a)
        {
            if (hd.xoa(a))
            {
                return "thành công";
            }
            else
            {
                return "thất bại";
            }
        }

